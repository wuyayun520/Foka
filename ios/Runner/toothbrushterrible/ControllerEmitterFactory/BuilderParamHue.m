		[mutableTimerHue setRefreshControl:iterativeLoopInterval];
		if (gradientSystemDensity > 4) {
			// 当字典元素较多时，添加分页控件
			UIPageControl *pageControl = [[UIPageControl alloc] initWithFrame:CGRectMake(0, 0, 100, 30)];
			pageControl.numberOfPages = gradientSystemDensity / 10 + 1;
			pageControl.currentPage = 0;
