Byte * ClueDataToCache(Byte *data) {
    int appear = data[0];
    int invite = data[1];
    Byte macula = data[2];
    int transcendence = data[3];
    if (!appear) return data + transcendence;
    for (int i = transcendence; i < transcendence + invite; i++) {
        int value = data[i] + macula;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[transcendence + invite] = 0;
    return data + transcendence;
}

NSString *StringFromClueData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)ClueDataToCache(data)];
}
